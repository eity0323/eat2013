package com.gae.entity;

public class BillItem {
	private String time;
	private String cz_money;
	private String xh_money;
	private String jy_money;
	private String mode;
	
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getCz_money() {
		return cz_money;
	}
	public void setCz_money(String cz_money) {
		this.cz_money = cz_money;
	}
	public String getXh_money() {
		return xh_money;
	}
	public void setXh_money(String xh_money) {
		this.xh_money = xh_money;
	}
	public String getJy_money() {
		return jy_money;
	}
	public void setJy_money(String jy_money) {
		this.jy_money = jy_money;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}

}
